function swapPropertiesLink(dest) {
    var lang_before = $('#dropdown_main_link');
    var lang_to_go = $(dest);

    var id_dest_a = lang_to_go.attr('id');
    var id_flag_dest_a = lang_to_go.children('.flag-icon').attr('id');
    var id_text_dest_a = lang_to_go.children('.text_en_c').attr('id');
    var label_dest = lang_to_go.attr('label_language');

    //var id_orig_a = lang_before.attr('id');
    const id_flag_orig_a = lang_before.children('.flag-icon').attr('id');
    const id_text_orig_a = lang_before.children('.text_en_c').attr('id');
    const label_orig = lang_before.attr('label_language');
    const complete_label = lang_before.attr('complete_label');

    //lang_before.attr('id', '#'.concat(id_dest_a));
    lang_before.attr('complete_label', id_dest_a);
    lang_before.children('.flag-icon').removeClass('flag-icon-'.concat(label_orig)).addClass('flag-icon-'.concat(label_dest)).attr('id', id_flag_dest_a);
    lang_before.children('.text_en_c').html("  ".concat(label_dest.toUpperCase())).attr("id", id_text_dest_a);
    lang_before.attr('label_language', label_dest);

    lang_to_go.attr('id', complete_label);
    lang_to_go.children('.flag-icon').removeClass('flag-icon-'.concat(label_dest)).addClass('flag-icon-'.concat(label_orig)).attr('id', id_flag_orig_a);
    lang_to_go.children('.text_en_c').html("  ".concat(label_orig.toUpperCase())).attr("id", id_text_orig_a);
    lang_to_go.attr('label_language', label_orig);

}

